namespace MouseGestureIssue.MouseWheelGestures
{
	/// <summary>
	///		Determines the direction of the mouse wheel
	///		to capture by <see cref="MouseWheelGesture"/>
	/// </summary>
	public enum WheelDirection
	{
		/// <summary>
		///		No movement.
		/// </summary>
		None,

		/// <summary>
		///		Turning the mouse wheel upwards.
		/// </summary>
		Up,

		/// <summary>
		///		Turning the mouse wheel downwards.
		/// </summary>
		Down
	}
}
